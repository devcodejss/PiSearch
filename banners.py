# Función para limpiar la terminal dependiendo del sistema operativo
def limpiar_terminal():
    sistema = os.name
    if sistema == 'nt':  # Windows
        os.system('cls')
    else:  # Unix-like (Linux, macOS)
        os.system('clear')



